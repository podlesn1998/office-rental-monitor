/**
 * Determine the district of a Saint Petersburg address.
 * Returns the district name or null if not identified.
 */
export function guessDistrict(address: string | null | undefined): string | null {
  if (!address) return null;
  const lower = address.toLowerCase();

  for (const rule of DISTRICT_RULES) {
    for (const kw of rule.keywords) {
      if (lower.includes(kw.toLowerCase())) {
        return rule.district;
      }
    }
  }

  return null;
}
